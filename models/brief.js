var mongoose = require("mongoose");

var briefSchema = new mongoose.Schema({
   firstname: String,
   lastname: String,
   author: {
      id: {
         type: mongoose.Schema.Types.ObjectId,
         ref: "User"
      },
      username: String
   },
   comments: [
      {
         type: mongoose.Schema.Types.ObjectId,
         ref: "Comment"
      }
   ]
});

module.exports = mongoose.model("Brief", briefSchema);